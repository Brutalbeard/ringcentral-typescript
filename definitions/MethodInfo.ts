class MethodInfo
{
    /// <summary>
    /// Method identifier. The default value is 1 (Ground)
    /// Enum: 1, 2, 3
    /// </summary>
    id: string

    /// <summary>
    /// Method name, corresponding to the identifier
    /// Enum: Ground, 2 Day, Overnight
    /// </summary>
    name: string
}

export default MethodInfo