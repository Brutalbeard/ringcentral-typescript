class CountryInfo
{
    /// <summary>
    /// Internal identifier of a home country
    /// </summary>
    id: string

    /// <summary>
    /// Canonical URI of a home country
    /// </summary>
    uri: string

    /// <summary>
    /// Official name of a home country
    /// </summary>
    name: string

    /// <summary>
    /// ISO code of a country
    /// </summary>
    isoCode: string

    /// <summary>
    /// Calling code of a country
    /// </summary>
    callingCode: string
}

export default CountryInfo