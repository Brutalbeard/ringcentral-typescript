class PromptInfo
{
    /// <summary>
    /// Internal identifier of a prompt
    /// </summary>
    uri: string

    /// <summary>
    /// Link to a prompt metadata
    /// </summary>
    id: string

    /// <summary>
    /// Type of a prompt media content
    /// </summary>
    contentType: string

    /// <summary>
    /// Link to a prompt media content
    /// </summary>
    contentUri: string

    /// <summary>
    /// Name of a prompt
    /// </summary>
    filename: string
}

export default PromptInfo